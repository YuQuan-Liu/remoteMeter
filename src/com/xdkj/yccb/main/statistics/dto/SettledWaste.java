package com.xdkj.yccb.main.statistics.dto;

public class SettledWaste {

	private String settleTime;
	private int nread;
	private int slaveSum;
	public String getSettleTime() {
		return settleTime;
	}
	public void setSettleTime(String settleTime) {
		this.settleTime = settleTime;
	}
	
	public int getNread() {
		return nread;
	}
	public void setNread(int nread) {
		this.nread = nread;
	}
	public int getSlaveSum() {
		return slaveSum;
	}
	public void setSlaveSum(int slaveSum) {
		this.slaveSum = slaveSum;
	}
	public SettledWaste() {
		super();
	}
	
	
}
