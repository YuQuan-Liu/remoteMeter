package com.xdkj.yccb.main.readme.dto;

public class WasteReadView {

	private int pid;
	private String louNum;
	private int meterRead;
	private int salveSum;
	private int waste;
	private int wasted;
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getLouNum() {
		return louNum;
	}
	public void setLouNum(String louNum) {
		this.louNum = louNum;
	}
	public int getMeterRead() {
		return meterRead;
	}
	public void setMeterRead(int meterRead) {
		this.meterRead = meterRead;
	}
	public int getSalveSum() {
		return salveSum;
	}
	public void setSalveSum(int salveSum) {
		this.salveSum = salveSum;
	}
	public int getWaste() {
		return waste;
	}
	public void setWaste(int waste) {
		this.waste = waste;
	}
	public int getWasted() {
		return wasted;
	}
	public void setWasted(int wasted) {
		this.wasted = wasted;
	}
	public WasteReadView() {
		super();
	}
	
	
}
